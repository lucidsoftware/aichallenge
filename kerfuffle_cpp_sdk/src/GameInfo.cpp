/**********************************************************************************************************************
 *********************************************************************************************************************/
Player* Players::findPlayerById(int playerId) const
{
	for (auto it = begin(); it != end(); ++it)
	{
		Player* player = it->second.get();
		if (player->id == playerId)
		{
			return player;
		}
	}

	return nullptr;
}

