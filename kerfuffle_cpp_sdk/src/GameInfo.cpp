/**********************************************************************************************************************
 *********************************************************************************************************************/
GameInfo::GameInfo() :
	boardWidth(0),
	boardHeight(0),
	gameOver(false)
{
}

void GameInfo::reset()
{
	boardWidth = 0;
	boardHeight = 0;
	gameOver = false;
	partialBoard.reset();
	players.clear();
}
